public class Intro {
    // 05. 변수(Variable)는 변하는 수입니다.
    // 변수는 언제 쓰나요?

    // - 프로그램에서는 항상 변하는 값을 나타낼 필요가 있다.
    // - 표현하려는 수에 맞는 데이터 타입(자료형)을 이용하여 변수를 선언
    // - 표현하려는 자료가 숫자, 문자, 문자열 등 다양할 수 있으므로 그에 맞는 자료형을 사용
    // - 변수 이름의 시작은 숫자로 시작할 수 없다.
    // - 자바에서 이미 예약어로 사용하는 이름은 변수로 사용할 수 없다.
    // - 변수 이름을 지을 때, 가독성을 꼭 고려하자.
    // - 변수 이름은 카멜케이스, 스네이크케이스를 사용하곤 하는데 보통 카멜케이스를 많이 사용한다.
}
