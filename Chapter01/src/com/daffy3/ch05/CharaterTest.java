package com.daffy3.ch05;

public class CharaterTest {
    public static void main(String[] args) {

        char charater1 = 'A';
        System.out.println(charater1); // A
        System.out.println((int)charater1); // 65

        char charater2 = 66;
        System.out.println(charater2); // B

        int charater3 = 67;
        System.out.println((char)charater3); // C

    }
}
